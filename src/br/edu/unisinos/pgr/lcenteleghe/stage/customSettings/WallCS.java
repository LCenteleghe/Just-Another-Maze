package br.edu.unisinos.pgr.lcenteleghe.stage.customSettings;

public class WallCS{
	private float x;
	private float y;
	
	public WallCS(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
	
}